@dataclass(frozen=True)
class Banner:
    name: str
    text: str

    @property
    def width(self) -> int:
        return max((len(line) for line in self.lines), default=0)

    @property
    def lines(self) -> tuple[str, ...]:
        return tuple(line.rstrip() for line in self.text.strip("\n").splitlines())

    def render(self) -> str:
        return "\n".join(self.lines)


BANNERS = {
    "big": Banner(name="big", text=LOGO_BIG),
    "small": Banner(name="small", text=LOGO_SMALL),
}


def banner_for_width(width: int, *, padding: int = 4) -> Banner:
    if width >= BANNERS["big"].width + padding:
        return BANNERS["big"]
    return BANNERS["small"]


def render_banner(width: int) -> str:
    return banner_for_width(width).render()
